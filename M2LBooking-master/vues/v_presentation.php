    <h4 class="mt-5">Présentation de la Maison de la Ligue</h4>
    <p>

        <img src="./public/image/mrsl_2.jpg">
    </p>
    <br/>
    <br/>
    La Maison des Ligues de Lorraine (M2L) a pour mission de fournir des espaces et des services aux différentes ligues
    sportives régionales et à d’autres structures hébergées.<br/>
    <br/>

    La M2L est une structure financée par le Conseil Régional de Lorraine dont l'administration est déléguée au Comité
    Régional Olympique et Sportif de Lorraine (CROSL).<br/>
    <br/>
    Véritable lieu de vie, cette Maison propose aux Ligues et Comités, des locaux fonctionnels situés à l'est de Nancy,
    permettant ainsi aux dirigeants, aux bénévoles et aux salariés d'échanger, de partager, de se former et de se
    regrouper dans des conditions optimales. Ce sont ici plus de 3 550 clubs lorrains, toutes disciplines confondues,
    qui bénéficient de cet outil. Un tel établissement est à la fois un facteur fort de cohésion et de qualité du sport
    régional.
<br/>
    <br/>
    Installée depuis 2003 dans la banlieue Nancéienne, la M2L accueille l'ensemble du mouvement sportif Lorrain
    qui représente près de 6 500 clubs, plus de 525 000 licenciés et près de 50 000 bénévoles.
